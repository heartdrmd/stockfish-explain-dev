// One-time build script — reads Lichess's chess-openings TSVs from
// vendor/openings/*.tsv and emits src/openings_lichess.js as a plain
// JS module that the runtime can `import` without network access.
//
// Output shape: export const LICHESS_OPENINGS = [
//   { eco: 'B90', name: 'Sicilian Defense: Najdorf Variation', moves: ['e4','c5',...] },
//   …
// ];

import { readFileSync, writeFileSync } from 'node:fs';

const files = ['a', 'b', 'c', 'd', 'e'].map(c => `vendor/openings/${c}.tsv`);
const all = [];
let skipped = 0;

for (const path of files) {
  const text = readFileSync(path, 'utf8');
  const lines = text.split('\n').filter(l => l.trim());
  // Skip header row.
  for (const row of lines.slice(1)) {
    const parts = row.split('\t');
    if (parts.length < 3) { skipped++; continue; }
    const [eco, name, pgn] = parts;
    if (!eco || !name || !pgn) { skipped++; continue; }
    // Strip move-number tokens like "1." and "13..." from the PGN.
    // Also drop NAGs ($1), comments (in braces), and results.
    const sanTokens = pgn
      .replace(/\{[^}]*\}/g, '')
      .replace(/\([^)]*\)/g, '')
      .replace(/\$\d+/g, '')
      .split(/\s+/)
      .filter(Boolean)
      .filter(t => !/^\d+\.(\.\.)?$/.test(t) && !/^(1-0|0-1|1\/2-1\/2|\*)$/.test(t));
    if (!sanTokens.length) { skipped++; continue; }
    all.push({ eco, name, moves: sanTokens });
  }
}

console.log(`Parsed ${all.length} Lichess openings (skipped ${skipped} malformed rows)`);

// Sort deterministically by ECO + name for predictable diffs.
all.sort((a, b) => a.eco.localeCompare(b.eco) || a.name.localeCompare(b.name));

const out = `// GENERATED by scripts/build-lichess-openings.mjs — do not hand-edit.
// Source: https://github.com/lichess-org/chess-openings (CC0).
// Re-generate by running: node scripts/build-lichess-openings.mjs
//
// Count: ${all.length} named openings & sub-variations (ECO A00-E99).

export const LICHESS_OPENINGS = ${JSON.stringify(all, null, 0)};
`;
writeFileSync('src/openings_lichess.js', out);
console.log(`Wrote src/openings_lichess.js (${(out.length / 1024).toFixed(1)} KB)`);
